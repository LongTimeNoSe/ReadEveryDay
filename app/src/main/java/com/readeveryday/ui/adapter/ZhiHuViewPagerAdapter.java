package com.readeveryday.ui.adapter;

import android.content.Context;
import android.support.v4.view.PagerAdapter;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ImageView;

import com.bumptech.glide.Glide;
import com.readeveryday.bean.zhihu.TopStories;

import java.util.List;

/**
 * Created by XuYanping on 2017/3/20.
 */

public class ZhiHuViewPagerAdapter extends PagerAdapter {

    List<? extends View> mViews;

    public ZhiHuViewPagerAdapter(List<? extends View> views) {
        mViews = views;
    }

    @Override
    public int getCount() {
        return mViews.size();
    }

    @Override
    public boolean isViewFromObject(View view, Object object) {
        return view.equals(object);
    }

    @Override
    public Object instantiateItem(ViewGroup container, int position) {

        container.addView(mViews.get(position));
        return mViews.get(position);
    }

    @Override
    public void destroyItem(ViewGroup container, int position, Object object) {
        container.removeView(mViews.get(position));
    }
}
